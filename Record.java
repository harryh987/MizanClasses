package com.example.harry.db4;

import java.util.ArrayList;

public class Record {
    int x;
    int y;
    public Record(int p_x, int p_y)
    {
        this.x = p_x;
        this.y = p_y;
    }

    public int getX()
    {
        return this.x;
    }
    public int getY()
    {
        return this.y;
    }
}
